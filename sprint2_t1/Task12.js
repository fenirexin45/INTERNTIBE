let numb = 20;

console.log("\t\tPOS/NEG EVE/ODD");

if (numb > 0) {
    console.log("\nThe number is positive.");
} else if (numb < 0) {
    console.log("\nThe number is negative.");
} else {
    console.log("\nThe number is zero.");
}

if (numb % 2 == 0) {
    console.log("\nThe number is even.");
} else {
    console.log("\nThe number is odd.");
}